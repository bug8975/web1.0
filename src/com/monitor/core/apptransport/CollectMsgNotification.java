package com.monitor.core.apptransport;

public class CollectMsgNotification {

	private String monitorLineName;
	private long startTime;
	private long stopTime;
	private long sampleInterval;			//采集间隔
	public String getMonitorLineName() {
		return monitorLineName;
	}
	public void setMonitorLineName(String monitorLineName) {
		this.monitorLineName = monitorLineName;
	}
	public long getStartTime() {
		return startTime;
	}
	public void setStartTime(long startTime) {
		this.startTime = startTime;
	}
	public long getStopTime() {
		return stopTime;
	}
	public void setStopTime(long stopTime) {
		this.stopTime = stopTime;
	}
	public long getSampleInterval() {
		return sampleInterval;
	}
	public void setSampleInterval(long sampleInterval) {
		this.sampleInterval = sampleInterval;
	}
	@Override
	public String toString() {
		return "CollectMsgNotification [monitorLineName=" + monitorLineName + ", startTime=" + startTime + ", stopTime="
				+ stopTime + ", sampleInterval=" + sampleInterval + "]";
	}
}
